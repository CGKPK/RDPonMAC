// Extended scancode (E0 prefix) mapping
static const int16_t extended_scancode_to_keycode[128] = {
    [0x1C] = 0x4C,   // Numpad Enter
    [0x1D] = 0x3E,   // Right Control
    [0x35] = 0x4B,   // Numpad Divide
    [0x38] = 0x3D,   // Right Alt -> Right Option
    [0x47] = 0x73,   // Home
    [0x48] = 0x7E,   // Up Arrow
    [0x49] = 0x74,   // Page Up
    [0x4B] = 0x7B,   // Left Arrow
    [0x4D] = 0x7C,   // Right Arrow
    [0x4F] = 0x77,   // End
    [0x50] = 0x7D,   // Down Arrow
    [0x51] = 0x79,   // Page Down
    [0x52] = 0x72,   // Insert (-> Help on Mac)
    [0x53] = 0x75,   // Delete
    [0x5B] = 0x37,   // Left Windows -> Left Command
    [0x5C] = 0x36,   // Right Windows -> Right Command
    [0x5D] = 0x6E,   // Menu -> Context Menu (F13 area)
};

int16_t rdp_scancode_to_mac_keycode(uint8_t scancode, uint16_t flags)
{
    if (scancode >= 128)
        return -1;

    if (flags & RDP_KBD_FLAGS_EXTENDED) {
        return extended_scancode_to_keycode[scancode];
    }

    return scancode_to_keycode[scancode];
}
